// Generate car image URL from brand, model, color using imagin.studio CDN (free, no API key needed)
function carImageUrl(brand, model, color) {
  const make = encodeURIComponent(brand.toLowerCase());
  const family = encodeURIComponent(model.split(" ")[0].toLowerCase());
  const paint = encodeURIComponent(color.toLowerCase());
  return `https://cdn.imagin.studio/getimage?customer=img&make=${make}&modelFamily=${family}&paintDescription=${paint}&angle=29&zoomType=fullscreen&width=800`;
}

