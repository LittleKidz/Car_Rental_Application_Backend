async function seed() {
  try {
    await mongoose.connect(process.env.MONGO_URI);
    console.log("Connected to MongoDB\n");
    const dc = await Car.deleteMany({});
    const dp = await Provider.deleteMany({});
    console.log(
      `Cleared ${dp.deletedCount} providers, ${dc.deletedCount} cars\n`,
    );
    const created = await Provider.insertMany(providers);
    console.log(`Created ${created.length} providers`);

    const cars = C.map((c) => ({
      brand: c.brand,
      model: c.model,
      color: c.color,
      licensePlate: c.licensePlate,
      dailyRate: c.dailyRate,
      available: c.available,
      image: carImageUrl(c.brand, c.model, c.color),
      provider: created[c.p]._id,
    }));

    const cc = await Car.insertMany(cars);
    console.log(`Created ${cc.length} cars (with auto-generated images)\n`);

    console.log("─".repeat(60));
    for (let i = 0; i < created.length; i++) {
      const all = C.filter((c) => c.p === i);
      const av = all.filter((c) => c.available).length;
      console.log(
        `  ${String(i + 1).padStart(2)}) ${created[i].name.padEnd(26)} ${String(all.length).padStart(2)} cars (${av} avail)`,
      );
    }
    console.log("─".repeat(60));
    console.log(`  Total: ${created.length} providers, ${cc.length} cars`);
    console.log("\n  Image URLs generated from: cdn.imagin.studio");
    console.log("  (brand + model + color = exact car image)\n");
    console.log("Done!");
    process.exit(0);
  } catch (err) {
    console.error("Error:", err.message);
    process.exit(1);
  }
}
seed();
